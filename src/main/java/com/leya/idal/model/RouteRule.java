package com.leya.idal.model;

public class RouteRule {

    private String db_rule;

    private String table_rule;

    public String getDb_rule() {
        return db_rule;
    }

    public void setDb_rule(String db_rule) {
        this.db_rule = db_rule;
    }

    public String getTable_rule() {
        return table_rule;
    }

    public void setTable_rule(String table_rule) {
        this.table_rule = table_rule;
    }

}
